#include "fdf.h"

void	print(int **tab, int width, int *leng)
{
	int x = 0;
	int y = 0;
	while (y < width)
	{
		while (x < leng[y])
		{
			printf("%d ", tab[y][x]);
			x++;
		}
		printf("\n");
		x = 0;
		y++;
	}
}